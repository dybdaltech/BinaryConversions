def ascii_converter(number):
    return(ascii_chars[number])

def big_input(big):
    big = str(big).split()
    new_text = ""
    for num in big:
        num = int(num)
        try:
            new = ascii_converter(num)
        except:
            new = str(num)
        if new == "space":
            new_text += " "
        else:
            new_text += new
    return new_text

def text_to_ascii(t):
    l = ""
    for word in str(t):
        for letter in word:
            if letter == " ":
                letter = "space"
            l += str(text_ascii[letter])
            l += " "
    return l